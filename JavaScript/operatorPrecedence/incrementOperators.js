// Increment Operators
// ++	Postfix Increment	i++
let postfixIncrement = 5;
console.log(`Initial Value: ${postfixIncrement}`);
postfixIncrement++;
console.log(`Postfix Increment ${postfixIncrement}`);

console.log(`----------------------------`);

// Increment Operators
// ++	Pretfix Increment	++i
let prefixIncrement = 5;
console.log(`Initial Value: ${prefixIncrement}`);
++prefixIncrement;
console.log(`Prefix Increment ${prefixIncrement}`);
